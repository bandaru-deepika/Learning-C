/*
Input Output Library Functions
1. printf() and scanf()
2. gets() and puts()
3. getchar() and putchar()
4. fgets() and fputs()
5. fgetc() and fputc()

printf() and scanf()
- printf() is used to display the output on the screen.
- scanf() is used to read the input from the keyboard.

Syntax:
printf("format string", arguments);
scanf("format string", arguments);

gets() and puts()
- gets() is used to read a string from the keyboard.
- puts() is used to display a string on the screen.

Syntax:
gets(string);
puts(string);

getchar() and putchar()
- getchar() is used to read a character from the keyboard.
- putchar() is used to display a character on the screen.

Syntax:
getchar();
putchar(character);

fgets() and fputs()
- fgets() is used to read a string from the file.
- fputs() is used to display a string on the screen.

Syntax:
fgets(string, size, file pointer);
fputs(string, file pointer);

fgetc() and fputc()
- fgetc() is used to read a character from the file.
- fputc() is used to display a character on the screen.

Syntax:
fgetc(file pointer);
fputc(character, file pointer);


*/