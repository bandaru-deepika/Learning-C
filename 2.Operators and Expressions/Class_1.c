/*
Arithmetic Operators or Mathematical Operators

+	Addition
-	Subtraction
*	Multiplication
/	Division
%	Modulus

Increment and Decrement Operators

++	Increment
--	Decrement

Relational Operators

==	Equal to
!=	Not equal to
>	Greater than
<	Less than
>=	Greater than or equal to
<=	Less than or equal to

Logical Operators

&&	Logical AND - all conditions must be true
||	Logical OR - at least one condition must be true
!	Logical NOT - reverses the result

Bitwise Operators

&	Bitwise AND - all bits must be 1
|	Bitwise OR - at least one bit must be 1
^	Bitwise XOR - only one bit must be 1
~	Bitwise NOT - reverses all bits
<<	Bitwise left shift - shifts bits to the left
>>	Bitwise right shift - shifts bits to the right

Assignment Operators

=	Assigns values from right side operands to left side operand
+=	Add AND - adds right operand to the left operand and assign the result to left operand
-=	Subtract AND - subtracts right operand from the left operand and assign the result to left operand
*=	Multiply AND - multiplies right operand with the left operand and assign the result to left operand
/=	Divide AND - divides left operand with the right operand and assign the result to left operand
%=	Modulus AND - takes modulus using two operands and assign the result to left operand
<<=	Left shift AND - left shifts the bits of the left operand, the right operand decides the number of places to shift
>>=	Right shift AND - right shifts the bits of the left operand, the right operand decides the number of places to shift
&=	Bitwise AND - does bitwise AND on operands and assign the result to left operand
^=	Bitwise exclusive OR - does bitwise exclusive OR on operands and assign the result to left operand
|=	Bitwise inclusive OR - does bitwise inclusive OR on operands and assign the result to left operand

Conditional Operators

?: - Condition ? True Expression : False Expression

Sizeof Operator

sizeof() - returns the size of a variable

Comma Operator

, - used to separate multiple expressions

*/

