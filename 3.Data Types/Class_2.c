/*
Format Specifier
%c	Character
Eg: printf("%c", 'A');
%d	Signed decimal integer
Eg: printf("%d", 10);
%e	Scientific notation (mantissa/exponent)
e+value - 10 power + values
e-value - 10 power - values
Eg: printf("%e", 10.5);
Eg: printf("%e", 10e5);
%f	Floating point real number
Eg: printf("%f", 10.5);
%g	Use the shortest representation: %e or %f
Eg: printf("%g", 10.5);
%i	Signed decimal integer
Eg: printf("%i", 10);
%o	Unsigned octal
Eg: printf("%o", 10);
%s	String of characters
Eg: printf("%s", "Hello World");
%u	Unsigned decimal integer
Eg: printf("%u", 10);
%x	Unsigned hexadecimal integer
Eg: printf("%x", 10);
%X	Unsigned hexadecimal integer (capital letters)
Eg: printf("%X", 10);
%%	A % followed by another % character will write a single % to the screen
Eg: printf("%%");

*/