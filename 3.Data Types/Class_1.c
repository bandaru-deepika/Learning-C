/*
Data modifers are used to modify the data types.
There are two types of data modifiers in C:
- Signed
- Unsigned

Signed:
- The signed modifier is used to indicate that a variable can hold both positive and negative values.
- The signed modifier is used by default.

Syntax:
signed <data type> <variable name> = <value>;

Example:
signed int a = 10;

Unsigned:
- The unsigned modifier is used to indicate that a variable can hold only positive values.

Syntax:
unsigned <data type> <variable name> = <value>;

Example:
unsigned int a = 10;

*/