/*
.c - C source code file
.h - C header file
.o or .obj - object file
.exe - executable file

process of compiling a C program:
1. Preprocessing
2. Compilation
3. Assembly
4. Linking

Preprocessing:
- The preprocessor is a program that processes the source code before compilation.
- It is invoked by the compiler.

Compilation:
- The compiler translates the source code into assembly language or object code.
- The compiler is invoked by the linker.

Linking:
- The linker combines the object code with other object code and libraries to create an executable file.
- The linker is invoked by the operating system.
*/
