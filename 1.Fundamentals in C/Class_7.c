/*
Identifiers:
Identifiers are names given to entities like variables, functions, arrays, etc. 
They help to differentiate one entity from another.

Rules for writing identifiers:
1. An identifier name can be a combination of letters, digits and underscore(_).
Eg: name, name1, name_1, name1_, _name1
2. An identifier name must begin with a letter or an underscore.
Eg: _name, _1, _name1, name_1, name1_
3. Keywords cannot be used as identifiers.
4. An identifier name is case sensitive.
Eg: Name, name, NAME are different identifiers.
5. There is no length limit for an identifier name.
Eg: name12345678901234567890123456789012345678901234567890123456789012345678901234567890

Naming Conventions:
1. Camel Case: Each word in the identifier name is written with the first letter in upper case.
Eg: camelCase, camelCaseName, camelCaseName1, camelCaseName_1, camelCaseName1_, _camelCaseName1
2. Pascal Case: Each word in the identifier name is written with the first letter in upper case.
Eg: PascalCase, PascalCaseName, PascalCaseName1, PascalCaseName_1, PascalCaseName1_, _PascalCaseName1
3. Snake Case: Each word in the identifier name is separated by an underscore and written in lower case.
Eg: snake_case, snake_case_name, snake_case_name1, snake_case_name_1, snake_case_name1_, _snake_case_name1

Eg:
int main()
{
    int a = 10;
    int b = 20;
    int c = a + b;
    printf("%d", c);
    return 0;
}
Eg:
int a[10];
*/


