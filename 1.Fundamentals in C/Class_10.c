/*
Declaration of Variables
- Declaration of variables is used to define the type of data that can be stored in a variable.
- Declaration of variables is used to define the name of the variable.
- Declaration of variables is used to define the size of the variable.

Syntax:
<data type> <variable name>;

Example:
int a;
float b;
double c;
char d;
void e;


Initialization of Variables
- Initialization of variables is used to assign a value to a variable at the time of declaration.

Syntax:
<data type> <variable name> = <value>;

Example:
int a = 10;
float b = 10.5;
double c = 10.5;
char d = 'A';
void e = NULL;

*/


