/*

backslash characters:
backslash character is used to escape the special meaning of the next character.

\t - tab - mostly used - 4 spaces
\b - backspace - not mostly used
\r - carriage return - not mostly used 
\f - form feed - not mostly used
\a - alert - not mostly used
\v - vertical tab - not mostly used
\\ - backslash character - mostly used
\' - single quote - mostly used
\" - double quote - mostly used

string - "Hello World"
string with double quotes - "Hello \"World\"" /
string containing backslash - "Hello \\World" // Hello \World

*/

